package comma

import "testing"

func TestComma(t *testing.T) {
	tests := []struct {
		input string
		want  string
	}{
		{"1", "1"},
		{"-1", "-1"},
		{"+814", "+814"},
		{"-814", "-814"},
		{"-814.123", "-814.123"},
		{"5678.12345", "5,678.12345"},
		{"+5678.12345", "+5,678.12345"},
		{"-5678.12345", "-5,678.12345"},
		{"567891.3", "567,891.3"},
		{"+567891.3", "+567,891.3"},
		{"-567891.3", "-567,891.3"},
		{"99999999999999.99", "99,999,999,999,999.99"},
		{"+99999999999999.99", "+99,999,999,999,999.99"},
		{"-99999999999999.99", "-99,999,999,999,999.99"},
	}

	for _, test := range tests {
		got := Comma(test.input)
		if got != test.want {
			t.Errorf("Comma(%s) = %s", test.input, got)
		}
	}
}
